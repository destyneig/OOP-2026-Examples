// If-else
var month = 5;
if (month === 1) {
  console.log('January');
} else if (month === 2) {
  console.log('February');
} else {
  console.log('Other month');
}

// Switch
switch (month) {
  case 1: console.log('January'); break;
  case 2: console.log('February'); break;
  default: console.log('Other month');
}

// Loops
for (var i = 0; i < 10; i++) {
  console.log(i);
}

var j = 0;
while (j < 10) {
  console.log(j);
  j++;
}